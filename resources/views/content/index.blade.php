                    <h1>很遗憾， Python 可能会步 PHP 后尘。</h1>
                     <small class="gray"><a href="/member/WuwuGin">WuwuGin</a> · 4 小时 23 分钟前 · 3253 次点击   </small>
                </div>
                <div class="cell">
                    <div class="topic_content">
                        <div class="markdown_body">
                            <p>
                                都 2018 了 @baskice 作为萌娘百科的站长发的这个帖子，我陷思。
                            </p>
                            <blockquote>
                                <p>
                                    <a href="https://www.v2ex.com/t/461085" rel="nofollow">https://www.v2ex.com/t/461085</a>
                                </p>
                            </blockquote>
                            <p>
                                首先作为站长，我觉得有必要提醒一下，代码的不规范就是习惯问题，如果换语言并不能解决根本问题，其次大部分 web 业务的性能和语言特性基本无关，有时间换语言重构不如找找技术人员本身的毛病。
                            </p>
                            <p>
                                然后切入正题:
                            </p>
                            <p>
                                众所周知，PHP 作为很多其他行业转行 web 混饭吃的首选，加上培训班的毒害，大批良莠不齐的人混在一起，其后果就是被各位科班出身的人调侃称为“最好的语言”。我相信大部分人都明白，这些调侃大部分情况下与<strong>语言特性无关</strong>，更为重要的是，<strong>从业者本身在其中的口碑。</strong>
                            </p>
                            <p>
                                在本站大家也能见识到，部分 PHPer 因为基础不牢靠，会问出很多匪夷所思的问题出来，然后达成日常黑 PHP （ 1/n ）。我接触过某“著名”培训班出来的 PHPer，连基本的 mvc 的概念都抽象不出来，跟别说往上要求了，不过大部分 boss 也不会管这些，停留在业务<strong>能跑就行</strong>的层次，更加地降低这个本来就混乱的领域的门槛。
                            </p>
                            <p>
                                而另一方面，目前由于深度学习等概念的炒作（你 Python web 蹭你🐎呢），和国家将 Python 列入二级考试的风头，各培训班也开始蹭热度了，熙熙攘攘皆为利来，这无可厚非。不过结果是注定的，原本<strong>学院派</strong>的 Python 注定会涌入一大批培训班出生，“ 1 年工作经验”的求职人员，希望各位喜欢用语言鄙视链的大佬做好准备：）。
                            </p>
                            <p>
                                最后，原谅我可能标题党了，本帖并不涉及语言本身，只是有些现象其实正在发生。希望各位理性讨论。
                            </p>
                        </div>
